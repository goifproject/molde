package com.limefriends.molde.ui.menu_map.reportCard;


public class ReportCardItem {

    private String title;
    private String text;

    public ReportCardItem(String title, String text) {
        this.title = title;
        this.text = text;
    }

    public String getTitle() {
        return title;
    }
    public String getText() {
        return text;
    }
}
